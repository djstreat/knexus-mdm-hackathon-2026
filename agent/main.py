if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--db", type=str, required=True)
    parser.add_argument(
        "prompt",
        nargs="?",
        default=None,
        help="Question or task for the GCSS agent. If omitted, the agent summarizes the database schema.",
    )
    args = parser.parse_args()

    # Get Database schema
    schema = get_db_schema(args.db)
    user_prompt = args.prompt or DEFAULT_USER_PROMPT.format(schema=schema)

    # GCSS Agent
    agent = Agent(
        model=create_gemini_api_model(),
        system_prompt=SYSTEM_PROMPT.format(schema=schema),
        deps_type=GCSSAgentDeps,
        output_type=AgentReport,
        tools=gcss_tools,
    )

    # Call agent, return answer
    deps = GCSSAgentDeps(sqlite_db_path=args.db, db_schema=schema)
    answer = asyncio.run(agent.run(user_prompt=user_prompt, deps=deps))
    output = answer.output.model_copy(
        update={
            "queries_run": deps.queries_run,
            "system_alerts": deps.system_alerts,
        }
    )
    print(json.dumps(output.model_dump(mode="json"), indent=2))
