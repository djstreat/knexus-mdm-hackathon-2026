load_dotenv()


class GCSSAgentDeps(BaseModel):
    """Per-run dependencies and mutable state for the GCSS agent."""

    sqlite_db_path: str
    db_schema: str
    queries_run: list[str] = Field(default_factory=list)
    system_alerts: list["SystemAlert"] = Field(default_factory=list)


class SystemAlert(BaseModel):
    """A system alert created during an agent run."""

    alert_name: str
    alert_description: str
    severity: Literal["low", "medium", "high", "critical"] = "medium"
    tamcn: str | None = None
    sr_number: str | None = None
    serial_number: str | None = None
    created_at: datetime
    updated_at: datetime


class AgentReport(BaseModel):
    """Structured, auditable output from a GCSS agent run."""

    report: str = Field(description="Markdown-formatted report generated by the agent.")
    queries_run: list[str] = Field(
        default_factory=list,
        description="Exact SQL queries run by the agent while generating the report.",
    )
    system_alerts: list[SystemAlert] = Field(
        default_factory=list,
        description="System alerts generated by the agent run.",
    )


# ----- Utility Functions -----
def get_db_schema(db_path: str) -> str:
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("SELECT sql FROM sqlite_master WHERE type='table';")
    tables = cursor.fetchall()
    schema = "\n".join([table[0] for table in tables])
    conn.close()
    return schema


# ----- TOOLS -----
gcss_toolset = FunctionToolset[GCSSAgentDeps]()


@gcss_toolset.tool
def query_db(ctx: RunContext[GCSSAgentDeps], query: str) -> list[dict[str, Any]]:
    ctx.deps.queries_run.append(query)
    print(f"Executing query: {query}")

    conn = sqlite3.connect(ctx.deps.sqlite_db_path)
    conn.row_factory = sqlite3.Row
    cursor = conn.cursor()
    cursor.execute(query)
    rows = cursor.fetchall()
    results: list[dict[str, Any]] = [dict(row) for row in rows]
    conn.close()

    # Log results
    print(f"Results: {results}")
    return results


@gcss_toolset.tool
def create_system_alert(
    ctx: RunContext[GCSSAgentDeps],
    alert_name: str,
    alert_description: str,
    severity: Literal["low", "medium", "high", "critical"] = "medium",
    tamcn: str | None = None,
    sr_number: str | None = None,
    serial_number: str | None = None,
) -> None:
    """Creates a system alert, optionally linked to a TAMCN, service request, or serial number."""
    now = datetime.now(UTC)
    alert = SystemAlert(
        alert_name=alert_name,
        alert_description=alert_description,
        severity=severity,
        tamcn=tamcn,
        sr_number=sr_number,
        serial_number=serial_number,
        created_at=now,
        updated_at=now,
    )
    ctx.deps.system_alerts.append(alert)

    print(f"Created system alert: {alert_name}")

    conn = sqlite3.connect(ctx.deps.sqlite_db_path)
    cursor = conn.cursor()
    timestamp = format_utc_timestamp(now)
    cursor.execute(
        """
        INSERT INTO system_alerts (
            alert_name,
            alert_description,
            severity,
            tamcn,
            sr_number,
            serial_number,
            created_at,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        """,
        (
            alert.alert_name,
            alert.alert_description,
            alert.severity,
            alert.tamcn,
            alert.sr_number,
            alert.serial_number,
            timestamp,
            timestamp,
        ),
    )
    conn.commit()
    conn.close()


gcss_tools = [
    query_db,
    create_system_alert,
]

MODEL_NAME = "gemini-3.1-pro-preview"


def format_utc_timestamp(value: datetime) -> str:
    """Return a stable UTC timestamp string for SQLite storage."""
    return value.isoformat(timespec="seconds").replace("+00:00", "Z")


def create_vertex_model() -> GoogleModel:
    """Create the Vertex AI-backed model definition."""
    provider = GoogleProvider(vertexai=True)
    return GoogleModel(MODEL_NAME, provider=provider)


def create_gemini_api_model() -> GoogleModel:
    """Create the Gemini API-backed model definition using GEMINI_API_KEY."""
    provider = GoogleProvider(api_key=os.environ.get("GEMINI_API_KEY"), vertexai=False)
    return GoogleModel(MODEL_NAME, provider=provider)


